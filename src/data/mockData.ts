export interface User {
  id: string;
  name: string;
  email: string;
  password: string;
  title: string;
  unemploymentStart: string;
  avatar?: string;
  skills: Skill[];
  connections: string[];
  badges: UserBadge[];
  bio: string;
  failures: string[];
}

export interface Skill {
  name: string;
  endorsements: number;
}

export interface UserBadge {
  id: string;
  earned: boolean;
  earnedDate?: string;
}

export interface Post {
  id: string;
  userId: string;
  content: string;
  timestamp: string;
  likes: number;
  comments: number;
  likedBy: string[];
}

export interface Job {
  id: string;
  title: string;
  company: string;
  location: string;
  salary: string;
  type: string;
  description: string;
  requirements: string[];
  tags: string[];
  posted: string;
  applications: number;
}

export interface Badge {
  id: string;
  title: string;
  description: string;
  icon: string;
  requirement: string;
}

export interface Comment {
  id: string;
  postId: string;
  userId: string;
  content: string;
  timestamp: string;
}

export interface ConnectionRequest {
  id: string;
  fromUserId: string;
  toUserId: string;
  message?: string;
  timestamp: string;
  status: 'pending' | 'accepted' | 'rejected';
}

